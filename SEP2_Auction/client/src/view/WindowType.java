package view;

/**
 * The WindowType enum represents the different types of windows that can be opened in the application.
 */
public enum WindowType {
  /**
   * Represents the login window.
   */
  LOG_IN,

  /**
   * Represents the sign-up window.
   */
  SIGN_UP,

  /**
   * Represents the window for starting an auction.
   */
  START_AUCTION,

  /**
   * Represents the window for displaying an auction.
   */
  DISPLAY_AUCTION,

  /**
   * Represents the window for displaying all auctions.
   */
  ALL_AUCTIONS,

  /**
   * Represents the window for displaying the user's auctions.
   */
  MY_AUCTIONS,

  /**
   * Represents the window for displaying the user's bids.
   */
  MY_BIDS,

  /**
   * Represents the window for displaying notifications.
   */
  NOTIFICATIONS,

  /**
   * Represents the window for resetting the password.
   */
  RESET_PASSWORD,

  /**
   * Represents the window for displaying the user's profile.
   */
  DISPLAY_PROFILE,

  /**
   * Represents the window for editing the user's profile.
   */
  EDIT_PROFILE,

  /**
   * Represents the window for displaying all accounts.
   */
  ALL_ACCOUNTS
}
